class TTTNode:

    def __init__(self):
        self.data = None
        self.up = None
        self.down = None
        self.left = None
        self.right = None
    

    def getData(self):
        return self.data
    

    def setData(self, newData):
        self.data = newData
    

    def getUp(self):
        return self.up
    

    def getDown(self):
        return self.down
    

    def getRight(self):
        return self.right
    

